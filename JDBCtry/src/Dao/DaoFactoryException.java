package Dao;

public class DaoFactoryException extends RuntimeException {

	public DaoFactoryException() {
		super();
		
	}

	public DaoFactoryException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

	public DaoFactoryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public DaoFactoryException(String arg0) {
		super(arg0);
		
	}

	public DaoFactoryException(Throwable arg0) {
		super(arg0);
		
	}
	
}
